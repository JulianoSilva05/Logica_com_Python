#Acessando caracteres
'''
Podemos acessar os caracteres de uma string;
Para isso precisamos saber o ídice do caractere;
O ídice é a posição de uma letra, e sempre começa por 0;
Então a primeira letra tem o índice 0;
Caso o índice não exista em uma string, receberemos um erro do Python;
Ex.: print(palavra[3])
'''
palavra = "Texto"
print(palavra[3])
palavra = "Juliano"
print(palavra[0])
print(palavra[1])
print(palavra[2])
print(palavra[3])
print(palavra[4])
print(palavra[5])
print(palavra[6])
#Decomposição de string
'''
• Podemos resgatar apenas uma parte da string a parir dos indices dos caracteres;
• Esta prática é bem útil e conhecida também como MANIPULAÇÃO DE STRING
• Exemplo:
• print(frase[0:5]) → Retorna do primeiro ao quinto índice
'''
frase = "O rato roeu a roupa do rei de Roma"
tipo = (type(frase))
print(tipo)
print(len(frase))
print(frase[0:30])
animal = (frase[2:6])
print(animal)

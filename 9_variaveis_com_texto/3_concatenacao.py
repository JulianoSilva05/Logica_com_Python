#Concatenação
'''
•A ação de juntar duas strings é chamada de concatenação;
•E um recurso muito utilizado, ainda mais em conjunto de variáveis;
•Podemos formar frases ou unir duas variáveis por exemplo;
•O símbolo de concatenação no Python é o +
'''
nome = "Juliano"
print("Olá "+nome)

velocidade = "80"
carro = "Gol"
cidade = "Belo Horizonte"
print("O veículo "+carro+" estava a "+velocidade+"Km/h em "+cidade)
saudacao = "Olá"
print(saudacao+" "+nome)
sobrenome = "Silva"
espaco = " "
print(nome+espaco+sobrenome)
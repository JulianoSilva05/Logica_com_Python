#Estrutura de repetição - FOR
'''
O FOR é outra estrutura de repetição;
Em Python ele é mais utilizado com listas;
Porém podemos utilizá-lo com strings também.
'''
palavra = "Testando"
for letra in palavra:
    print (letra)

nome = "Juliano"

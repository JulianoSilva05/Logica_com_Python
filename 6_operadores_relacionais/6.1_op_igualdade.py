#Operadores == e !=
'''Significam igualdade e diferença;
O operador de == vai testar se os valores da esquerda e direita são iguais;
já o != vai testar se são diferentes;
O resultado desta operação retorna um Booleano
'''
a = 10
b = 10
c = 15

print(a == b)
print(b == c)
print(a != b)
print(c != b)
